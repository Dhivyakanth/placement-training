import java.util.Scanner;  
class Main 
{  
public static void main(String[] args)  
{  
Scanner sc= new Scanner(System.in);   
System.out.println("Enter first number");  
int n= sc.nextInt();
for(int i=1;i<=10;i+=2)  
    System.out.println(i);
while(n<=10)
    System.out.println(n);  
    n++;  
}  
}  
